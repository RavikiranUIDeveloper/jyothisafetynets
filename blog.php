                                    <div class="title">Pigeons pose health hazards</div>
                                    <p>Hyderabad: Cooing softly, they stutter around pecking at the ground, and are favourites of children who love to feed them. However, studies indicate that the seemingly harmless pigeons are not that safe for everyone.<br>
Pointing out that the rising pigeon population in the city could pose health threats to citizens, a few ornithologists are stressing on the need to control the pigeon population here.<br>
According to ornithologists, close association with pigeons could result in pulmonary disorders, besides fungal, bacterial and viral infections. This could be more in children compared to adults. Professor Jayashankar Telangana State Agricultural University Professor V. Vasudeva Rao has been conducting a study on pigeons in the city for the last four years.
Currently, there are over 480 feeding centres across the city with 200 to 40,000 birds being fed at each centre. A rough estimate is that the bird population has crossed over six lakh. All these statistics are pertaining to only 40 per cent of the city as the study is yet to cover the entire city limits, says Dr. Rao.
A pigeon consumes 18 to 22 gm of food every day and people are feeding nine different types of millets, grains, rice and other items. More importantly, the feeding centres are expanding to new areas rapidly. In recent times, feeding activity has reached Yapral, Bowenpally and a few other areas.
Considering the adverse effects of pigeons, a few cities like New Delhi have already imposed a ban on feeding these birds. In Hyderabad too, the Forest Department has banned feeding at KBR Park but people continue to ignore the ban, points out Dr. Rao.
This is mainly due to lack of awareness among people. Some take it as a sentiment, while others do it for fun. But the pigeon population needs to be controlled and cutting down the feeding will reduce the population by nearly 40 per cent, he suggests.
“Being an ornithologist, it is tough to make these recommendations but it is the need of the hour. While the pigeon population is increasing, population of other birds like sparrows and doves is declining. A comprehensive study should be conducted to observe all these factors and control the pigeon population,” he adds.
                                    </p>
